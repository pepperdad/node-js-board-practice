const mongoose = require('mongoose');

// Schema
const postSchema = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
  },
});

// model & export
const post = mongoose.model('post', postSchema);
module.exports = post;
